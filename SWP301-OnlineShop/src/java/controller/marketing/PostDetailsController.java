package controller.marketing;

import dal.CategoryPostDBContext;
import dal.PostDBContext;
import java.io.IOException;
import java.io.PrintWriter;
import javax.servlet.ServletException;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import model.Post;

/**
 *
 * @author DELL
 */
@WebServlet(name = "PostDetailsController", urlPatterns = {"/marketing/postDetails"})
public class PostDetailsController extends HttpServlet {

    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        int id = Integer.parseInt(request.getParameter("id"));
        PostDBContext postDB = new PostDBContext();
        Post post = postDB.getPostByIdIncludeCategory(id);

        request.setAttribute("post", post);
        request.getRequestDispatcher("../view/marketing/postDetails.jsp").forward(request, response);
    }

    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
    }

    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
